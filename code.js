//Obtener datos del formulario



function obtenerDatos (){
let clase = document.getElementsByName("clase")
let subClase = document.getElementsByName("subclase")
let desplazar =  document.getElementsByName("desplazar")
let dieta =  document.getElementsByName("dieta")
let nacer =  document.getElementsByName("nacer")
let extremidadesS = document.getElementsByName("extremidadesS")
let extremidadesI = document.getElementsByName("extremidadesI")
let total =  document.getElementsByName("total")
let sangre = document.getElementsByName("sangre")
let domestico = document.getElementsByName("domestico")
let bioma = document.getElementsByName("bioma")
    //clase activa

    for(let clas of clase){
        if(clas.checked){
            clase = clas.value
            
        }
    }

    for(let subclas of subClase){
        if(subclas.checked){
            subClase = subclas.value
        }
    }
    for(let des of desplazar){
        if(des.checked){
            desplazar = des.value
        }
    }
    for(let diet of dieta){
        if(diet.checked){
            dieta = diet.value
        }
    }
    for(let born of nacer){
        if(born.checked){
            nacer = born.value
        }
    }
    for(let xtremiS of extremidadesS){
        if(xtremiS.checked){
            extremidadesS = xtremiS.value
        }
    }
    for(let xtremisI of extremidadesI){
        if(xtremisI.checked){
            extremidadesI = xtremisI.value
        }
    }
    for(let tot of total){
        if(tot.checked){
            total = tot.value
        }
    }
    for(let blood of sangre){
        if(blood.checked){
            sangre = blood.value
        }
    }
    for(let domestic of domestico){
        if(domestic.checked){
            domestico = domestic.value
        }
    }
    for(let biom of bioma){
        if(biom.checked){
            bioma = biom.value
        }
    }
    
    calcularAnimal({
        clase: clase,
        subClase: subClase,
        habitad: desplazar,
        dieta: dieta,
        nacimiento: nacer,
        tipoExtremidadS: extremidadesS,
        tipoExtremidadI: extremidadesI,
        cantidadE: total,
        sangre: sangre,
        domesticable: domestico,
        bioma: bioma,
    })
    
}



